import java.util.Scanner;

public class Bak10872 {

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        int n = sc.nextInt();

        int pack = 1;

        for (int i = 1; i <= n ; i++) {
            pack *= 1;

        }

        System.out.println(pack);





    }
}
